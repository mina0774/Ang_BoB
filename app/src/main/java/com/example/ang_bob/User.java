package com.example.ang_bob;

public class User {

    public String email = "";
    public String username = "";
    public String gender = "";
    public String age = "";


    public User() {

    }


    public User(String email, String username, String gender, String age) {
        this.email = email;
        this.username = username;
        this.gender = gender;
        this.age = age;
    }

}